    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        int product_id = Integer.valueOf(idfield.getText());
        int pqty= Integer.valueOf(quantityfield.getText());
        boolean res=facade.updateQuantityOfProduct(product_id,pqty);
        if(res==true)
            JOptionPane.showMessageDialog(null, "Stock Successfully Updated!");
        else if(res==false){
            JOptionPane.showMessageDialog(null, "Entry does not exist!");
        } 
    }//GEN-LAST:event_jButton1MouseClicked

    private void admintableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_admintableMouseClicked
        // TODO add your handling code here:
        int row = admintable.getSelectedRow();
        String click = (admintable.getModel().getValueAt(row,1).toString());
        idfield.setText(click);
        deletefield.setText(click);
    }//GEN-LAST:event_admintableMouseClicked

    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        // TODO add your handling code here:
        int delid = Integer.valueOf(deletefield.getText());
        boolean res = facade.deleteProductFromInventory(delid);
        if(res==true)
            JOptionPane.showMessageDialog(null, "Item Deleted!");
        else if(res==false){
            JOptionPane.showMessageDialog(null, "Entry does not exist!");
        }
        generateAdminPanel();
    }//GEN-LAST:event_jButton2MouseClicked

