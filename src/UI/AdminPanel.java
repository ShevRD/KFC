    public void generateAdminPanel(){
        ArrayList<Products> list = facade.manageInventory();
        String[] columnName = {"Product Name", "ID","Quantity","Category"};
        Object[][] rows = new Object[list.size()][4];
        for(int i = 0; i < list.size(); i++){
            rows[i][0] = list.get(i).getName();
            rows[i][1] = list.get(i).getId();
            rows[i][2] = list.get(i).getQuantity();
            rows[i][3] = list.get(i).getCategory();
        }
        
        TheModel model = new TheModel(rows, columnName);
        admintable.setModel(model);
        admintable.setRowHeight(40);
        admintable.getColumnModel().getColumn(3).setPreferredWidth(40);
        setLocation(350,200);

    }
    
