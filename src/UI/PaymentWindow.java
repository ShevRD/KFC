    public boolean onlyDigits(String str){
        String regex = "[0-9]+";
        Pattern p = Pattern.compile(regex); 
        if (str == null) { 
            return false; 
        }
        Matcher m = p.matcher(str); 
        return m.matches(); 
    }
    

