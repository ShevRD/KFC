    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        
        String s = jTextField1.getText();
        String h = jTextField3.getText();
        String j = jTextField5.getText();
        String r = jTextField4.getText();
        boolean res = facade.isAuthorized(s,h,j,r);
        if(res== false){
            JOptionPane.showMessageDialog(null,"Card Details Invalid or Incorrect Data Type!"+"\n"+"Please Try Again");
        }else{
        jLayeredPane1.removeAll();
        jLayeredPane1.add(jPanel2);
        jLayeredPane1.repaint();
        jLayeredPane1.revalidate();
        t = new Timer(1000, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                k++;
                if(k==5){
                    dispose();
                    ArrayList<String> list  = new ArrayList<>();
                    list = facade.grabDetails();
                    String c = list.get(0);
                    String q = list.get(1);
                    String status = "NEW!";
                    String regex = "(?<=[\\d])(,)(?=[\\d])";
                    Pattern p = Pattern.compile(regex);
                    String str1 = billstr;
                    Matcher m = p.matcher(str1);
                    str1 = m.replaceAll("");
                    int tcost = Integer.parseInt(str1);
                    String cd = c.replaceAll(", $", "");
                    String qh = q.replaceAll(", $", "");
                    boolean res2 = facade.placeOrder(UName,UID,cd,tcost,status,qh,email);
                    if(res2==true){
                        JOptionPane.showMessageDialog(null,"Payment Successful!");
                        JOptionPane.showMessageDialog(null, "Order Placed");
                    }else if(res2==false){
                        JOptionPane.showMessageDialog(null, "Order Failed!");
                    };                    
                }
            }
        });
        t.start();
        }
    }//GEN-LAST:event_jButton1MouseClicked

